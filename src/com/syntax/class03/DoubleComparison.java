public class DoubleComparison {
	public static void main(String[] args) {
		double d1 = 150;
		double d2 = 200;
		
		if (d1 > d2) {
			System.out.println("double value 150 is smaller than 200");
		}else {
			System.out.println("double value 200 is larger than 150");
		}
		
		
		
	}

}
