package com.syntax.class09;

public class Patterns {
	public static void main(String[] args) {
		
		for (int i = 1; i <= 10; i++) {
			for (int a = 1; a <= 10; a++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		for (int i = 1; i <= 4; i++) {
			for (int a = 1; a <= 4; a++) {
				System.out.print(i);
			}
			System.out.println();
		}
		
	}

}
