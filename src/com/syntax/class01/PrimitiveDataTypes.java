package com.syntax.class01;

public class PrimitiveDataTypes {
	
	public static void main (String [] args) {
		/*  Primitive data types
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * doulbe
		 * char
		 * boolean
		 *
		 */
		
		// Whole number data types
		byte num = 10;
		
		short num1 = 100;
		
		int num2 = 1000;
		//l or L
		long num3 = 100000000000098657L;
		
		// Decimal Number
		float num4 = 12.9f;
		
		double num5 = 0.888;
		
		// Non-Number Data Types
		
		char add = '+';
		char letter = 'a';
		
		// tru or false (yes or no)
		
		boolean hungary = true ;
		
		boolean thirsty = false;
		
		
	}
}
